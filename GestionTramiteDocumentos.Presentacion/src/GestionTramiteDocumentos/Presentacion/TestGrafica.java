    public void mostrarGrafica1(){
        Integer[] datos = new Integer[10];
        Integer[] dias = new Integer[31];
        Integer[] numero = new Integer[31];
        
        for (int i = 0; i < datos.length; i++) {
            datos[i]=i*5;
        }

        for (int i = 0; i < dias.length; i++) {
            dias[i]=(i+1);
        }
        
        for (int i = 0; i < dias.length; i++) {
            int cont =0;
            for (int j = 0; j < datos.length; j++) {
                if(dias[i]==datos[j]){
                    cont++;
                }
            }
            numero[i]=cont;
        }
        
        Graficas grafica1 = new Graficas(Graficas.LINEAL, "", "dias", "NÂ° de documentos");
        grafica1.setParametros("", dias, numero);
        GraficaPNL.removeAll();
        GraficaPNL.add(grafica1.getPanel(), BorderLayout.CENTER);
        GraficaPNL.validate();
    }
    
